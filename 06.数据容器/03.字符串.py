name = "Kaito"
name.upper() # 转换为大写

name.lower() # 转换为小写

name.title() # 首字母大写

name.swapcase() # 大小写互换

name.capitalize() # 首字母大写

name.replace("K", "C") # 替换字符

name.split() # 以空格分割字符串

name.split("o") # 以"o"分割字符串

name.join(["K", "a", "i", "t", "o"]) # 以指定字符连接字符串

name.startswith("K") # 是否以"K"开头

name.endswith("o") # 是否以"o"结尾

name.find("t") # 查找子串的位置

name.count("t") # 统计子串出现的次数

name.isalpha() # 是否全为字母

name.isdigit() # 是否全为数字

name.isalnum() # 是否全为字母或数字

name.isspace() # 是否全为空白字符

