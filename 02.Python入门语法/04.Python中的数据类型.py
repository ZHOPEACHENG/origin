#通过print语句输出数据类型
print(type("Kaito"))
print(type(11281222))
print(type(6.22))



#使用变量储存type()语句的结果
string_type = type("Kaito")
int_type = type(11281222)
float_type = type(6.22)
print(string_type)
print(int_type)
print(float_type)



#使用type语句，查看变量中储存的数据类型
name = "Kaito"
name_type = type(name)
print(name_type)
