# %m.n : 表示控制宽度为m，控制小数精度为n（小数点占一个宽度）
num1 = 66
num2 = 6.22
print("数字6宽度限制5，结果是：%5d"%num1)
print("数字6宽度限制1，结果是：%1d"%num1) # 设置宽度小于数字本身不生效
print("数字6.22宽度限制3，小数精度2，结果是：%3.2f"%num2) # 设置宽度小于数字本身不生效
print("数字6.22宽度限制7，小数精度2，结果是：%7.2f"%num2)
print("数字6.22宽度限制3，小数精度1，结果是：%3.1f"%num2) # 小数精度小于小数本身，四舍五入
print("数字6.22宽度不限制，小数精度2，结果是：%.2f"%num2)

